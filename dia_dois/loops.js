// FOR

//   váriavel / condição / incremento
for (i = 10; i <= 20; i++) {
    console.log('Número ', i)
}

// WHILE

j = 0
while (j <= 5) {
    if (j === 3) break
    console.log('Número', j)
    j++
}

// DO WHILE, pouco utilizado

x = 0

do {
    console.log('Número', x)
    x++
} while (x <= 7)